/*

B - Shift only

入力例 1
Copy
3
8 12 40

出力例 1
Copy
2

*/

$num_02 = trim(fgets(STDIN));
$str_02 = trim(fgets(STDIN));

$arr_02 = explode(" ", $str_02);

$ruselt_02 = 0;
$h_num = $num_02 - 1;
while (true) {

    for ($i = 0; $i < $num_02; $i++) {

        // 割れなくなったらループを抜ける
        if ($arr_02[$i] % 2 == 1) {
            break 2;
        }

        // 値の更新処理 
        $arr_02[$i] = $arr_02[$i] / 2;

        // === 判定　カウントアップ
        if ($i == $h_num) {
            $ruselt_02 += 1;
        }
    } // === END for 

} // ================ END while 

// === 出力
echo $ruselt_02;

