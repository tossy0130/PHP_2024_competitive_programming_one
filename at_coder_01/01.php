$N_02 = fgets(STDIN);

$max_loop_num = 1; // 割った数
$max_count = 0; // 割れた数の最大

// === 大枠のループ（ 取得した値 ）
for ($i = 1; $i <= $N_02; $i++) {
    // print($i);
    $cnt = 0;
    $loop_val = $i;

    while ($loop_val % 2 == 0) {
        $loop_val /= 2;
        $cnt += 1;
    }

    // 値の更新
    if ($max_count < $cnt) {
        $max_loop_num = $i;
        $max_count = $cnt;
    }
} // ========= END for

// 何回目のループの数かを出力
