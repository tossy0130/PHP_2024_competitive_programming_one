$str_a = trim(fgets(STDIN)); // 500
$str_b = trim(fgets(STDIN)); // 100
$str_c = trim(fgets(STDIN)); // 50

$str_x = trim(fgets(STDIN)); // カウントアップ対象の値

$result_count = 0;

// === 全方法をループで試す
for ($i = 0; $i <= $str_a; $i++) {
    for ($j = 0; $j <= $str_b; $j++) {
        for ($z = 0; $z <= $str_c; $z++) {
            // === 判定　カウントアップ
            if (($i * 500) + ($j * 100) + ($z * 50) == $str_x) {
                $result_count += 1;
            }
        }
    }
}

print($result_count);

